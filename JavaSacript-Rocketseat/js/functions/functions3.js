//parametro (parameters)
const sum = function(number1, number2)
{
    let total = number1 + number2;
    return total;
}

let number1 = 34;
let number2 = 25;

console.log(`O primeiro numero é ${number1}`)
console.log(`O segundo numero é ${number2}`)
console.log(`A soma é ${sum(number1, number2)}`)
